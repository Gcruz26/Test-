create extension if not exists "pgcrypto";

do $$
begin
  if not exists (select 1 from pg_type where typname = 'user_role') then
    create type public.user_role as enum ('Admin', 'Finance', 'Operations', 'Viewer');
  end if;

  if not exists (select 1 from pg_type where typname = 'interpreter_status') then
    create type public.interpreter_status as enum ('Active', 'Inactive', 'On Hold');
  end if;

  if not exists (select 1 from pg_type where typname = 'payment_frequency') then
    create type public.payment_frequency as enum ('Weekly', 'Biweekly', 'Monthly');
  end if;
end
$$;

create table if not exists public.users (
  id uuid primary key references auth.users (id) on delete cascade,
  email text not null unique,
  full_name text not null,
  role public.user_role not null default 'Viewer',
  created_at timestamptz not null default timezone('utc', now())
);

